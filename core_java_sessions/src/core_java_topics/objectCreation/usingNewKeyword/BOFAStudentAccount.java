package core_java_topics.objectCreation.usingNewKeyword;

public class BOFAStudentAccount {

	private long std_acc_num;
	private String std_holder_name;
	private String branch_name;
	private double std_acc_balance;
	private double minimum_opening_balance;
	public long getStd_acc_num() {
		return std_acc_num;
	}
	public void setStd_acc_num(long std_acc_num) {
		this.std_acc_num = std_acc_num;
	}
	public String getStd_holder_name() {
		return std_holder_name;
	}
	public void setStd_holder_name(String std_holder_name) {
		this.std_holder_name = std_holder_name;
	}
	public String getBranch_name() {
		return branch_name;
	}
	public void setBranch_name(String branch_name) {
		this.branch_name = branch_name;
	}
	public double getStd_acc_balance() {
		return std_acc_balance;
	}
	public void setStd_acc_balance(double std_acc_balance) {
		this.std_acc_balance = std_acc_balance;
	}
	public double getMinimum_opening_balance() {
		return minimum_opening_balance;
	}
	public void setMinimum_opening_balance(double minimum_opening_balance) {
		this.minimum_opening_balance = minimum_opening_balance;
	}
	
}
