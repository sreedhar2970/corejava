package core_java_topics.methodsInJava.pre_defined_method;

public class PredefinedMethod {

	public static void main(String[] args) {
		String name = "kamran";
		System.out.println("Length of String name : " + name.length());
		System.out.println("The maximum number : " + Math.max(4, 9));
		System.out.println("The minimum number : " + Math.min(7, 67));
	}

}
